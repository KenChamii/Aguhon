public enum ConditionType
{
    Lason,      // Poisoned — loses HP each turn
    Lumpo,      // Paralyzed — chance to skip turn, persists until cured/battle ends
    Takot,      // Frightened — Disadvantage on attack rolls
    Bulag,      // Blinded — Disadvantage on accuracy, cannot gain Advantage
    Bawi,       // Restrained — Disadvantage on Bilis checks, cannot flee
    Pagpala     // Blessed (positive) — Advantage on next move
}